#import "ZQFMDBManager.h"
#import "FMDB.h"
@implementation ZQFMDBManager
{
    FMDatabase *_fmdb;
}
SingleImplementation(ZQFMDBManager)

- (instancetype)init {
    if (self = [super init]) {
        _fmdb = [FMDatabase databaseWithPath:@""];
    }
    return self;
}

- (FMDatabase *)fmdb {
    return _fmdb;
}

@end
